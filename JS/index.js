var state = {
    'querySet': tableData,

    'page': 1,
    'rows': 5,
    'window': 5,
}

$(document).ready(function () {
    buildTable()
});

function pagination(querySet, page, rows) {

    var trimStart = (page - 1) * rows
    var trimEnd = trimStart + rows

    var trimmedData = querySet.slice(trimStart, trimEnd)

    var pages = Math.round(querySet.length / rows);

    return {
        'querySet': trimmedData,
        'pages': pages,
    }
}

function pageButtons(pages) {
    const wrapper = $('#pagination-wrapper')

    wrapper.innerHTML = ``

    let maxLeft = (state.page - Math.floor(state.window / 2))
    let maxRight = (state.page + Math.floor(state.window / 2))

    if (maxLeft < 1) {
        maxLeft = 1
        maxRight = state.window
    }

    if (maxRight > pages) {
        maxLeft = pages - (state.window - 1)

        if (maxLeft < 1) {
            maxLeft = 1
        }
        maxRight = pages
    }

    if (state.page != 1) {
        wrapper.append(`<button value=${1} class="page btn btn-sm btn-info ml-1 ">&#171; First</button>` + wrapper.innerHTML);
    }

    for (let page = maxLeft; page <= maxRight; page++) {
        if (state.page == page) {
            wrapper.append(`<button value=${page} class="page active btn btn-sm btn-secondary ml-1">${page}</button>`);
        } else {
            wrapper.append(`<button value=${page} class="page btn btn-sm btn-primary ml-1">${page}</button>`);     
        }
    }

    if (state.page != pages) {
        wrapper.append(`<button value=${pages} class="page btn btn-sm btn-info ml-1">Last &#187;</button>`);
    }

    $('.page').on('click', function () {
        $('#table-body').empty()
        $('#pagination-wrapper').empty()

        state.page = Number($(this).val())

        buildTable()
    })

}


function buildTable() {

    const data = pagination(state.querySet, state.page, state.rows)
    const myList = data.querySet

    myList.forEach(function (dato, index) {

        $('#table-body').append(`
               <tr>
               <td>${dato.rank}</td>
               <td>${dato.first_name}</td>
               <td>${dato.last_name}</td>
               </tr>`);
    });

    pageButtons(data.pages)
}
